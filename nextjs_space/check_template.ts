import { PrismaClient } from '@prisma/client'

const prisma = new PrismaClient()

async function main() {
  const templates = await prisma.template.findMany({
    select: {
      id: true,
      name: true,
      svgPath: true,
      svgIsPublic: true,
      layerData: true,
    }
  })
  console.log(JSON.stringify(templates, null, 2))
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect())
